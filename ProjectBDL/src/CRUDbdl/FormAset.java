    private void btnHapusAsetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusAsetActionPerformed
        // TODO add your handling code here:
        String kodeAset = getKodeAsetDipilih();
        if (kodeAset == null) {
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(null,
         "Apakah Anda yakin ingin menghapus aset dengan kode: " + kodeAset + "?\n"
         + "Tindakan ini tidak dapat dibatalkan.",
         "Konfirmasi Hapus",
         javax.swing.JOptionPane.YES_NO_OPTION,
         javax.swing.JOptionPane.WARNING_MESSAGE);

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            Connection conn = null;
            try {
                conn = ConnectDatabaseLoginBDL.getConnection();

                // 3. Execute Delete Query
                String sql = "DELETE FROM aset WHERE kode_aset_unik = ?";
                java.sql.PreparedStatement ps = conn.prepareStatement(sql);
                ps.setString(1, kodeAset);

                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                    javax.swing.JOptionPane.showMessageDialog(null, "Aset berhasil dihapus!");
                    muatDataAsetKeTabel(); // Refresh the table
                } else {
                    javax.swing.JOptionPane.showMessageDialog(null, "Gagal: Aset tidak ditemukan di database.");
                }

            } catch (java.sql.SQLException e) {
                // 4. Handle Foreign Key Constraints (SQLState 23xxx)
                // This happens if the asset is used in Maintenance or Depreciation tables
                if (e.getSQLState() != null && e.getSQLState().startsWith("23")) {
                    javax.swing.JOptionPane.showMessageDialog(null,
                     "Gagal Menghapus:\nAset ini memiliki data terkait (Riwayat Pemeliharaan/Penyusutan).\n"
                     + "Hapus data terkait terlebih dahulu jika ingin menghapus aset ini.",
                     "Data Terproteksi",
                     javax.swing.JOptionPane.ERROR_MESSAGE);
                } else {
                    javax.swing.JOptionPane.showMessageDialog(null, "Error Database: " + e.getMessage());
                }
            } catch (Exception e) {
                System.out.println("Error Hapus: " + e);
            } finally {
                try {
                    if (conn != null) {
                        conn.close();
                    }
                } catch (Exception ex) {
                }
            }
        }
    }//GEN-LAST:event_btnHapusAsetActionPerformed

    private void btnProsesPenyusutanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesPenyusutanActionPerformed
        // TODO add your handling code here:
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
         "Apakah Anda yakin ingin memproses penyusutan untuk periode ini?\n"
         + "Proses ini akan mengurangi Nilai Buku aset berdasarkan Umur Ekonomisnya.",
         "Konfirmasi Penyusutan", javax.swing.JOptionPane.YES_NO_OPTION);

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        java.sql.Connection conn = null;
        try {
            conn = Login.ConnectDatabaseLoginBDL.getConnection();
            conn.setAutoCommit(false);

            String sqlGet = "SELECT a.kode_aset_unik, a.harga_perolehan, a.nilai_buku, k.umur_ekonomis_tahun "
             + "FROM aset a "
             + "JOIN kategori k ON a.id_kategori = k.id_kategori "
             + "WHERE a.nilai_buku > 0 AND k.umur_ekonomis_tahun > 0";

            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(sqlGet);

            String sqlUpdateAset = "UPDATE aset SET nilai_buku = ? WHERE kode_aset_unik = ?";
            PreparedStatement psUpdate = conn.prepareStatement(sqlUpdateAset);

            String sqlInsertHistory = "INSERT INTO penyusutan (kode_aset_unik, tanggal_hitung, metode, nilai_penyusutan, nilai_buku_akhir) VALUES (?, CURRENT_DATE, ?, ?, ?)";
            PreparedStatement psInsert = conn.prepareStatement(sqlInsertHistory);

            int countProcessed = 0;

            while (rs.next()) {
                String kode = rs.getString("kode_aset_unik");
                double hargaBeli = rs.getDouble("harga_perolehan");
                double nilaiBukuLama = rs.getDouble("nilai_buku");
                int umurTahun = rs.getInt("umur_ekonomis_tahun");

                double penyusutanPerBulan = hargaBeli / (umurTahun * 12);

                // Calculate New Value
                double nilaiBukuBaru = nilaiBukuLama - penyusutanPerBulan;
                double nilaiPenyusutanNyata = penyusutanPerBulan;

                // Handle logic if asset value drops below 0
                if (nilaiBukuBaru < 0) {
                    nilaiBukuBaru = 0;
                    nilaiPenyusutanNyata = nilaiBukuLama;
                }

                psUpdate.setDouble(1, nilaiBukuBaru);
                psUpdate.setString(2, kode);
                psUpdate.addBatch();

                psInsert.setString(1, kode);
                psInsert.setString(2, "Garis Lurus");
                psInsert.setDouble(3, nilaiPenyusutanNyata);
                psInsert.setDouble(4, nilaiBukuBaru);
                psInsert.addBatch();

                countProcessed++;
            }

            psUpdate.executeBatch();
            psInsert.executeBatch();

            conn.commit();
            javax.swing.JOptionPane.showMessageDialog(this,
             "Penyusutan Selesai!\n" + countProcessed + " aset telah disusutkan nilainya.");

            muatDataAsetKeTabel();

        } catch (Exception e) {
            try {
                if (conn != null) {
                    conn.rollback();
                }
            } catch (Exception ex) {
            }
            System.out.println("Error Penyusutan: " + e);
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal Proses: " + e.getMessage());
        } finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (Exception ex) {
            }
        }

    }//GEN-LAST:event_btnProsesPenyusutanActionPerformed

