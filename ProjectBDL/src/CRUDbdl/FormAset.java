    private void muatDataAsetKeTabel() {
        DefaultTableModel model = (DefaultTableModel) tblAset.getModel();
        model.setRowCount(0);

        try {
            Connection conn = ConnectDatabaseLoginBDL.getConnection();

            // Use JOIN to get names instead of IDs
            String sql = "SELECT a.kode_aset_unik, a.nama_aset, k.nama_kategori, "
             + "l.nama_lokasi, p.nama_pegawai, a.nilai_buku "
             + "FROM aset a "
             + "LEFT JOIN kategori k ON a.id_kategori = k.id_kategori "
             + "LEFT JOIN lokasi l ON a.id_lokasi = l.id_lokasi "
             + "LEFT JOIN pegawai p ON a.id_pegawai = p.id_pegawai "
             + "ORDER BY a.kode_aset_unik ASC";

            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                // Format Rupiah
                double nilai = rs.getDouble("nilai_buku");
                java.text.NumberFormat cur = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));
                String nilaiFormatted = cur.format(nilai);

                model.addRow(new Object[]{
                    rs.getString("kode_aset_unik"),
                    rs.getString("nama_aset"),
                    rs.getString("nama_kategori"), // Shows "Elektronik" instead of "KAT-01"
                    rs.getString("nama_lokasi"), // Shows "Gudang" instead of "LOK-01"
                    rs.getString("nama_pegawai"), // Shows "Budi" instead of "101"
                    nilaiFormatted
                });
            }
            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load aset: " + e.getMessage());
        }
    }

